package com.visog.pasupukumkuma.res;

public class PujaSamagriPujaRes {

    private String id;
	
	private String pujasamagri;
	
	private String pujas;
	
	private String status;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPujasamagri() {
		return pujasamagri;
	}

	public void setPujasamagri(String pujasamagri) {
		this.pujasamagri = pujasamagri;
	}

	public String getPujas() {
		return pujas;
	}

	public void setPujas(String pujas) {
		this.pujas = pujas;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
