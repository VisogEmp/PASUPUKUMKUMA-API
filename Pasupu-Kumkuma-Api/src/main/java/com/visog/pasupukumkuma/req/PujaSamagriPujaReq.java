package com.visog.pasupukumkuma.req;

public class PujaSamagriPujaReq {


	private String pujasamagri;
	
	private String pujas;
	
	private String status;

	public String getPujasamagri() {
		return pujasamagri;
	}

	public void setPujasamagri(String pujasamagri) {
		this.pujasamagri = pujasamagri;
	}

	public String getPujas() {
		return pujas;
	}

	public void setPujas(String pujas) {
		this.pujas = pujas;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
